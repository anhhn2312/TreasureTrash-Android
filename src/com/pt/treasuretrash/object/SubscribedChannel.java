package com.pt.treasuretrash.object;

public class SubscribedChannel extends BaseObject{

	private String userId;
	private String channelName;
	private long lastViewDate;
	private String awayId;
	private String itemId;
	
	
	public SubscribedChannel(){}
	
	public SubscribedChannel(String userId, String channelName, long lastViewDate, String awayId, String itemId){
		this.userId = userId;
		this.channelName = channelName;
		this.lastViewDate = lastViewDate;
		this.awayId = awayId;
		this.itemId = itemId;
	}
	
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getChannelName() {
		return channelName;
	}
	public void setChannelName(String channelName) {
		this.channelName = channelName;
	}

	public long getLastViewDate() {
		return lastViewDate;
	}

	public void setLastViewDate(long lastViewDate) {
		this.lastViewDate = lastViewDate;
	}

	public String getAwayId() {
		return awayId;
	}

	public void setAwayId(String awayId) {
		this.awayId = awayId;
	}

	public String getItemId() {
		return itemId;
	}

	public void setItemId(String itemId) {
		this.itemId = itemId;
	}
	
}
